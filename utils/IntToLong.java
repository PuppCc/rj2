package com.easyse.easyse_simple.utils;

/**
 * @author: zky
 * @date: 2022/11/20
 * @description: int类型转化为Long
 */
public class IntToLong {

    /**
     * int类型转化为Llong
     * @param value 要转化的值
     * @return
     */
    public static Long toLong(int value) {
        return new Long(value);
    }
}
